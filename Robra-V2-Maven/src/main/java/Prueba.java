    SerialPortEventListener listener = new SerialPortEventListener(){
        
        @Override
        public void serialEvent(SerialPortEvent spe) {
            try{
            if (ino.isMessageAvailable()){
                jLabelResult.setText(ino.printMessage());//ino.printMessage imprime lo que se recibe del arduino
            }
            }
            catch(SerialPortException ex){
                Logger.getLogger(Window.class.getName()).log(Level.SEVERE, null, ex);
            }
            catch(ArduinoException ex){
                Logger.getLogger(Window.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    };
  
    public Prueba() {
        initComponents();
        
        try {
            
            String puertoStr = ino.getSerialPorts().toString();
            System.out.println("Hola pauli este es el puerto: "+puertoStr + " lelelelele");
            
            ino.arduinoRXTX(puertoStr, 115200, listener);
        } catch (ArduinoException ex) {
            Logger.getLogger(Prueba.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

