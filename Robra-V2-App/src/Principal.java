    private void abrirArchivoCSV() {
        JFileChooser fc=new JFileChooser();
        int returnVal = fc.showOpenDialog(this);

        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File file = fc.getSelectedFile();
            abrirCSV(file.getAbsolutePath());
        } 

    }

    private void abrirCSV(String absolutePath) {
        DefaultTableModel mat=null;
                
        try {
            CSVReader csvReader = new CSVReader(new FileReader(absolutePath));
            String[] fila = null;
            int i=0;
            while((fila = csvReader.readNext()) != null) {
                if(i==0) mat=new DefaultTableModel(fila, 0);
                else mat.addRow(fila);
                System.out.println(fila[0]+ " | " + fila[1]+ " |  " + fila[2]);
                i++;
            }
            csvReader.close();
        } catch (IOException ex) {
            Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(mat!=null) tabla.setModel(mat);

            }
